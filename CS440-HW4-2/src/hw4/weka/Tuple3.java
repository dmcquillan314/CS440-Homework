package hw4.weka;

public class Tuple3<T, V, W> {

	T first;
	V second;
	W third;
	
	public T getFirst() {
		return first;
	}
	public void setFirst(T first) {
		this.first = first;
	}
	public V getSecond() {
		return second;
	}
	public void setSecond(V second) {
		this.second = second;
	}
	public W getThird() {
		return third;
	}
	public void setThird(W third) {
		this.third = third;
	}
	
}
